package strings;

public class StringConcatenation {
    public static void main(String[] args) {
        String str1 = "Just a";
        String str2 = " string";
        String str3 = "";
        str3 = str1 + str2;

        System.out.println(str1);
        System.out.println(str2);
        System.out.println(str3);
    }
}
